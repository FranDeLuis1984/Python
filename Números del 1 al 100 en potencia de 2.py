 # importamos la libreria math para calcular la potencia de los números recorridos en el bucle

powers_of_2 = [2 ** i for i in range(1, 101)] # El bucle for recorre todos los números del al 100 

# Mostramos los resultados
print("Potencias de 2 del 1 al 100:")
print(powers_of_2) # Imprimimos por pantalla los valores de las potencias. Las potencias siempre se elevan con el parametro ** Para ello, lo que tenemos que hacer es invocar al método powers

