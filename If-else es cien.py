""" IF-ELSE || Producto de dos numeros igual a 100"""

# DATOS DE ENTRADA

numero_1 = int (input("Dame un número: "))
numero_2 = int (input("Dame otro número: "))

if numero_1 * numero_2 == 100:

    print("El producto de los dos numeros es igual a 100")

else:

    if numero_1 * numero_2 != 100:

        print ("El producto de los dos números es distinto de 100")

