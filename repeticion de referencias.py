
# Creamos una colección (lista) de 4 elementos

mi_lista = [1,2,3,4] # Los elementos de la lista tienen la siguiente secuencia 1 (0), 2 (1), 3 (2), 4 (3)

# Guardamos en la constante A el valor de mi_lista

A = [mi_lista] * 3 # El operador repetición concatena un número repetido de veces (en este caso 3)

# Imprimimos el valor de A

print (A)

# Ahora asignamos el valor 50 al elemento 2 de la lista (valor 3)

mi_lista [2] = 50

# De nuevo, volvemos a imprimir por pantalla la lista, con el valor modificado

print (A)